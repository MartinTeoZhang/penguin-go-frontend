          <!-- 用户信息 -->
          <span class="user-info"><img :src="user.avatar" />{{user.name}}</span>
          <el-popover ref="popover-personal" placement="bottom-end" trigger="click" :visible-arrow="false">
            <personal-panel :user="user" ></personal-panel>
          </el-popover>
        </el-menu-item>
      </el-menu>
    </span>
  </div>
