<script>
  import { mapState } from 'vuex'
  import mock from "@/mock/index";
  import Hamburger from "@/components/Hamburger"
  import ThemePicker from "@/components/ThemePicker"
  import LangSelector from "@/components/LangSelector"
  import Action from "@/components/Toolbar/Action"
  import NoticePanel from "@/views/Core/NoticePanel"
  import MessagePanel from "@/views/Core/MessagePanel"
  import PersonalPanel from "@/views/Core/PersonalPanel"
  import { baseUrl } from '@/utils/global'

  export default {
    components:{
      Hamburger,
      ThemePicker,
      LangSelector,
      Action,
      NoticePanel,
      MessagePanel,
      PersonalPanel
    },
    data() {
      return {
        user: {
          name: "Ryzin",
          avatar: "",
          role: "超级管理员",
          registInfo: "注册时间：2020-04-05 "
        },
        activeIndex: '1',
        langVisible: false
      };
    },
    methods: {
      openWindow(url) {
        window.open(url)
      },
      selectNavBar(key, keyPath) {
        console.log(key, keyPath)
      },
      //折叠导航栏
      onCollapse: function() {
        // 改为发送提交请求来改变收缩状态，this.
        this.$store.commit('onCollapse');
      },
      // 切换主题
      onThemeChange: function(themeColor) {
        // 设置 store 状态，保存共享主题色，这样其他绑定主题色的组件都可以自动更新了
        this.$store.commit('setThemeColor', themeColor)
      },
      // 语言切换
      changeLanguage(lang) {
        lang = lang === '' ? 'zh_cn' : lang
        this.$i18n.locale = lang
        this.langVisible = false
      },

      //加载头像
      loadAvatar(){
        this.$api.profile.getAvatarByUserName({name:sessionStorage.getItem("user")}).then((res)=>{
          if (res.msg === "no record"){
            //没有检查到头像，加载默认头像
            this.user.avatar = require("@/assets/user.png");
          }else{
            this.user.avatar = baseUrl +'/'+ res.data.path;
          }
        })
      },

      //加载个人基本信息
      loadBasicInfo(){
        this.$api.profile.getBasicInfo({name :sessionStorage.getItem("user")}).then((res)=>{
          this.user.role = res.data.roleNames;
          console.log(this.user.role);
        })
      },
      
    },



    mounted() {
      this.sysName = "主被试助手";
      var user = sessionStorage.getItem("user");
      if (user) {
        this.user.name = user;
        //this.user.avatar = require("@/assets/user.png");
        this.loadAvatar();
        this.loadBasicInfo();
      }
    },
    computed:{
      ...mapState({
        themeColor: state=>state.app.themeColor,
        collapse: state=>state.app.collapse
      })
    }
  };
</script>

